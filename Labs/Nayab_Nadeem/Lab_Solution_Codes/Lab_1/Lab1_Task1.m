% LAB 1 - TASK 1

% A car is traveling 10.2 miles/hour. Convert this speed to
% kilometers/hour.


% -------------------------------------------------------------------------

% Set the conversion rate
km_to_mph = 1.6;

% Convert 10.2 mph to kph
kph = 10.2 * km_to_mph;

% Display the final value
disp(['10.2 miles/hour is ', num2str(kph),' kilometers/hour.']);

